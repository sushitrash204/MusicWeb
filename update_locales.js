Object.keys(translations).forEach(lang => {
    const filePath = path.join(__dirname, 'public', 'locales', lang, 'common.json');
    if (fs.existsSync(filePath)) {
        const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
        const updates = translations[lang];
        const merged = { ...existing, ...updates };
        fs.writeFileSync(filePath, JSON.stringify(merged, null, 4));
        console.log(`\u2713 Updated ${lang}`);
    } else {
        console.log(`! Skipped ${lang} (file not found)`);
    }
});
